    class brg extends Barang {
        int id_barang, harga;
        String nama_barang, status;
        
        public brg(){
         this.nama_barang = text_nama_barang.getText();   
         this.harga = Integer.parseInt(text_harga.getText());
         this.status = combo_status_barang.getSelectedItem().toString();
        }
    }
    
    public void refreshTable() {
        model = new DefaultTableModel();
        model.addColumn("ID Barang");
        model.addColumn("Nama Barang");
        model.addColumn("Harga");
        model.addColumn("Status");
        
        tabel_barang.setModel(model);
        
        try{
            this.stat = k.getCon().prepareStatement("SELECT * FROM barang");
            this.rs = this.stat.executeQuery(); 
            while(rs.next()){
                Object[] data = {
                    rs.getInt("id_barang"),
                    rs.getString("nama_barang"),
                    rs.getInt("harga"),
                    rs.getString("status")
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_id_barang.setText("");
        text_nama_barang.setText("");
        text_harga.setText("");
    }

